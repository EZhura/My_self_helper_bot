PARENT_MAP = {}
for parent_screen, rows in SCREEN_BUTTONS.items():
    for row in rows:
        for button_text, target_screen in row:
            if target_screen != "start_screen" and target_screen in SCREEN_TEXTS:
                PARENT_MAP[target_screen] = parent_screen

# Для всех конечных экранов автоматически добавляем кнопки Назад / В меню,
# если для них ещё не задана отдельная клавиатура.
for screen_id in SCREEN_TEXTS:
    if screen_id not in SCREEN_BUTTONS and screen_id in PARENT_MAP:
        SCREEN_BUTTONS[screen_id] = [[("Назад", PARENT_MAP[screen_id]), ("В меню", "start_screen")]]

BUTTON_TO_SCREEN = {}
for screen_id, rows in SCREEN_BUTTONS.items():
    for row in rows:
        for button_text, target_screen in row:
            BUTTON_TO_SCREEN[button_text] = target_screen


def build_keyboard(screen_id: str) -> ReplyKeyboardMarkup:
    rows = SCREEN_BUTTONS.get(screen_id, SCREEN_BUTTONS["start_screen"])
    keyboard = [[KeyboardButton(button_text) for button_text, _ in row] for row in rows]
    return ReplyKeyboardMarkup(keyboard, resize_keyboard=True)


def is_allowed(update: Update) -> bool:
    if not ALLOWED_USER_IDS:
        return True
    user = update.effective_user
    return bool(user and user.id in ALLOWED_USER_IDS)


async def show_screen(update: Update, screen_id: str) -> None:
    if not update.message:
        return

    if not is_allowed(update):
        await update.message.reply_text("Доступ к этому боту ограничен.")
        return

    text = SCREEN_TEXTS.get(screen_id, "Экран пока не найден.")
    keyboard = build_keyboard(screen_id)
    await update.message.reply_text(text, reply_markup=keyboard)


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    await show_screen(update, "start_screen")


async def menu_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    await show_screen(update, "start_screen")


async def help_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if not update.message:
        return

    if not is_allowed(update):
        await update.message.reply_text("Доступ к этому боту ограничен.")
        return

    await update.message.reply_text(
        "Команды:\n"
        "/start — открыть главное меню\n"
        "/menu — открыть главное меню\n"
        "/help — краткая помощь\n\n"
        "Используйте кнопки меню, чтобы быстро находить готовые ответы.",
        reply_markup=build_keyboard("start_screen"),
    )


async def my_id_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """Показывает Telegram ID пользователя. Полезно для настройки ALLOWED_USER_IDS."""
    if not update.message or not update.effective_user:
        return

    await update.message.reply_text(f"Ваш Telegram ID: {update.effective_user.id}")


async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    if not update.message or not update.message.text:
        return

    if not is_allowed(update):
        await update.message.reply_text("Доступ к этому боту ограничен.")
        return

    text = update.message.text.strip()

    if text in BUTTON_TO_SCREEN:
        await show_screen(update, BUTTON_TO_SCREEN[text])
        return

    await update.message.reply_text(
        "Пока я понимаю только кнопки меню. Выберите нужный раздел ниже.",
        reply_markup=build_keyboard("start_screen"),
    )


def validate_settings() -> None:
    missing = []
    if not TOKEN:
        missing.append("TELEGRAM_BOT_TOKEN")
    if not PUBLIC_URL:
        missing.append("PUBLIC_URL")

    if missing:
        raise RuntimeError(
            "Не заданы обязательные переменные окружения: " + ", ".join(missing)
        )


def main() -> None:
    validate_settings()

    app = Application.builder().token(TOKEN).build()

    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("menu", menu_command))
    app.add_handler(CommandHandler("help", help_command))
    app.add_handler(CommandHandler("myid", my_id_command))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

    print("Bot Sales Helper 2.1 is running on Render...")

    app.run_webhook(
        listen="0.0.0.0",
        port=PORT,
        url_path=TOKEN,
        webhook_url=f"{PUBLIC_URL}/{TOKEN}",
        drop_pending_updates=True,
    )


if __name__ == "__main__":
    main()